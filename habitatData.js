const habitatDb = rawHabitatCsv.trim().split('\n').map((line, index) => {
    const cols = line.split(',');
    const displayId = cols[0].trim();
    const name = cols[1].trim();
    
    const materials = [];
    const pokemons = [];
    
    const isMaterial = (val) => {
        if (val.includes('×')) return true;
        if (val === '추가 예정') return true;
        if (val === '의자(긴 것)') return true;
        if (val === '접시에 올린 음식') return true;
        return false;
    };

    for (let i = 2; i < cols.length; i++) {
        const val = cols[i].trim();
        if (!val) continue;
        
        if (isMaterial(val)) {
            materials.push(val);
        } else {
            pokemons.push(val);
        }
    }
    
    const uniqueId = index + 1;
    const imageId = String(uniqueId).padStart(3, '0'); 
    
    return {
        uid: uniqueId, 
        displayId: displayId,
        name: name,
        materials: materials,
        pokemons: pokemons,
        imageUrl: `./images/habitat_${imageId}.png`
    };
});
